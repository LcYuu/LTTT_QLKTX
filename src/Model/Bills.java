package Model;

import java.util.Date;

/**
 *
 * @author NgoQuangNghia
 */
public class Bills {
    private int billId;
    private int roomId;
    private double price;
    private Date date;

    public Bills(int billId, int roomId, double price, Date date) {
        this.billId = billId;
        this.roomId = roomId;
        this.price = price;
        this.date = date;
    }

    public int getBillId() {
        return billId;
    }

    public void setBillId(int billId) {
        this.billId = billId;
    }

    public int getRoomId() {
        return roomId;
    }

    public void setRoomId(int roomId) {
        this.roomId = roomId;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
}

